export const getChoiceEffect = (choiceId: string): ChoiceEffectMapping | undefined => {
  return CHOICE_EFFECTS[choiceId];
};

export const getChoicesByEffect = (
  filter: Partial<Record<keyof ChoiceEffectMapping, number>>
): string[] => {
  return Object.entries(CHOICE_EFFECTS)
    .filter(([_, effect]) => {
      return Object.entries(filter).every(([key, value]) => {
        return effect[key as keyof ChoiceEffectMapping] === value;
      });
    })
    .map(([id]) => id);
};